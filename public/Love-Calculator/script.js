// Import and initialize the Appwrite client
const client = new Appwrite.Client();

client
    .setEndpoint("https://cloud.appwrite.io/v1") // Replace with your Appwrite endpoint
    .setProject("6780f44100185074ad33"); // Replace with your project ID

// Initialize the Databases module
const databases = new Appwrite.Databases(client);

// Define Database and Collection IDs
const databaseId = "6780f5140020681e525e"; // Replace with your Appwrite database ID
const collectionId = "6780f564000eb2946fc7"; // Replace with your Appwrite collection ID

window.onload = function () {
    const button = document.getElementById("calculate");
    button.addEventListener("click", calculateLove);
};

async function calculateLove() {
    const yourName = document.getElementById("fname").value.trim().toLowerCase();
    const crushName = document.getElementById("cname").value.trim().toLowerCase();

    if (yourName && crushName) {
        // Use the provided formula to calculate the love index
        const combined = yourName + crushName;
        const letterCounts = {};

        for (const letter of combined) {
            letterCounts[letter] = (letterCounts[letter] || 0) + 1;
        }

        // Sum of all letter counts
        const sum = Object.values(letterCounts).reduce((a, b) => a + b, 0);

        // Convert sum to a percentage
        const loveIndex = sum % 101;

        // Display the result
        document.getElementById("result-message").innerText = `${yourName} and ${crushName}'s chance of love:`;
        document.getElementById("result-percentage").innerText = `${loveIndex}%`;

        // Save result to Appwrite database
        try {
            const result = await databases.createDocument(
                databaseId,
                collectionId,
                "unique()", // Autogenerated document ID
                {
                    your_name: yourName,
                    crush_name: crushName,
                    percentage: loveIndex,
                    calculation_date: new Date().toISOString()
                }
            );
            console.log("Result saved to database!", result);
        } catch (error) {
            console.error("Failed to save result:", error.message);
        }
    } else {
        alert("Please enter both names!");
    }
}
